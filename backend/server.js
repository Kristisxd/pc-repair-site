require('dotenv').config();

const express = require('express');
const http = require('http');
const cors = require('cors');
const helmet = require('helmet');
const rateLimit = require('express-rate-limit');
const { Server } = require('socket.io');

const db = require('./db');
const { notifyOwner } = require('./push');
const { startReminderJob } = require('./cron/reminders');

const authRoutes = require('./routes/auth');
const appointmentRoutes = require('./routes/appointments');
const chatRoutes = require('./routes/chat');

const app = express();
const server = http.createServer(app);

const FRONTEND_URL = process.env.FRONTEND_URL || 'https://pc-repair-site.onrender.com';
const PORT = process.env.PORT || 4000;

console.log(process.env.ADMIN_EMAIL);
console.log(process.env.JWT_SECRET);

/* ---------------- Socket.IO ---------------- */
const io = new Server(server, {
  cors: {
    origin: FRONTEND_URL,
    methods: ['GET', 'POST'],
    credentials: true,
  },
});

/* ---------------- Security (FIXED) ---------------- */
app.use(
  helmet({
    contentSecurityPolicy: {
      directives: {
        /* Everything blocked by default */
        defaultSrc: ["'self'"],

        /* React bundle + runtime */
        scriptSrc: ["'self'"],

        /* Inline styles generated by React */
        styleSrc: ["'self'", "'unsafe-inline'"],

        /* Images incl. avatars, uploads */
        imgSrc: ["'self'", 'data:', 'blob:'],

        /* API + Socket.IO */
        connectSrc: [
          "'self'",
          FRONTEND_URL,
          `http://localhost:${PORT}`,
          `ws://localhost:${PORT}`,
          'https://yourdomain.com',
          'wss://yourdomain.com',
        ],

        /* Fonts */
        fontSrc: ["'self'", 'data:'],

        /* Disallow plugins & embeds */
        objectSrc: ["'none'"],
        frameSrc: ["'none'"],

        /* Lock base URI */
        baseUri: ["'self'"],

        /* Prevent clickjacking */
        frameAncestors: ["'none'"],

        /* Stop mixed content */
        upgradeInsecureRequests: [],
      },
    },

    /* Needed for Socket.IO + modern bundlers */
    crossOriginEmbedderPolicy: false,
  })
);

app.use(cors({ origin: FRONTEND_URL, credentials: true }));
app.use(express.json({ limit: '100kb' }));

/* ---------------- Rate limiting ---------------- */
const apiLimiter = rateLimit({
  windowMs: 60 * 1000,
  max: 120,
});
app.use('/api', apiLimiter);

/* ---------------- Routes ---------------- */
app.use('/api/auth', authRoutes);
app.use('/api/appointments', appointmentRoutes);
app.use('/api/chat', chatRoutes);

app.get('/api/health', (req, res) => res.json({ ok: true }));

/* ---------------- Real-time chat ---------------- */
io.on('connection', (socket) => {
  socket.on('join', ({ conversationId, isOwner }) => {
    if (isOwner) socket.join('owner');
    else if (conversationId) socket.join(conversationId);
  });

  socket.on('customer-message', ({ conversationId, body }) => {
    if (!conversationId || !body?.trim()) return;

    const text = body.trim().slice(0, 2000);

    db.run(
      `INSERT INTO chat_messages (conversation_id, sender, body)
       VALUES (?, 'customer', ?)`,
      [conversationId, text],
      function (err) {
        if (err) return;
        const message = {
          id: this.lastID,
          conversation_id: conversationId,
          sender: 'customer',
          body: text,
          created_at: new Date().toISOString(),
        };
        io.to(conversationId).emit('new-message', message);
        io.to('owner').emit('new-message', message);
        notifyOwner('New chat message', text, '/admin');
      }
    );
  });

  socket.on('owner-message', ({ conversationId, body }) => {
    if (!conversationId || !body?.trim()) return;

    const text = body.trim().slice(0, 2000);

    db.run(
      `INSERT INTO chat_messages (conversation_id, sender, body)
       VALUES (?, 'owner', ?)`,
      [conversationId, text],
      function (err) {
        if (err) return;
        const message = {
          id: this.lastID,
          conversation_id: conversationId,
          sender: 'owner',
          body: text,
          created_at: new Date().toISOString(),
        };
        io.to(conversationId).emit('new-message', message);
        io.to('owner').emit('new-message', message);
      }
    );
  });
});

/* ---------------- Jobs & Server ---------------- */
startReminderJob();

server.listen(PORT, () =>
  console.log(`PC Fix backend running on ${PORT}`)
);